    private void workAssignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_workAssignBtnActionPerformed
        String message = msgTxtField.getText();

        UserAccount receiver = (UserAccount) NurseComboBox.getSelectedItem();
        UserAccount donor = (UserAccount) donorComboBox.getSelectedItem();

        DonorDetails request = new DonorDetails();
        request.setMessage(message);
        request.setSender(userAccount);
        request.setReceiver(receiver);
        request.setDonor(donor.getEmployee());
        request.setStatus("Sent");

        receiver.getWorkQueue().getWorkRequestList().add(request);

        Organization org = null;
        for (Organization o : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (o instanceof NurseOrganization) {
                org = o;
                break;
            }
        }
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }

    }//GEN-LAST:event_workAssignBtnActionPerformed

