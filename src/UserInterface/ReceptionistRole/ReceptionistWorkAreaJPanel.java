    private void populateComboBox() {

        NurseComboBox.removeAllItems();
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof NurseOrganization) {
                for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
                    NurseComboBox.addItem(ua);
                }
            }
        }
    }

    public void populateDonorCombo() {
        donorComboBox.removeAllItems();
        for (Organization org : enterprise.getOrganizationDirectory().getOrganizationList()) {
            if (org instanceof DonorOrganization) {
                for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
                    donorComboBox.addItem(ua);
                }
            }
        }
    }
